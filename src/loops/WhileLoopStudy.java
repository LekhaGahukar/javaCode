package loops;

public class WhileLoopStudy {

	public static void main(String[] args) 
	{
	//if we want to print "A" 5 times
		int i=1;
		while(i<=5)
		{
			System.out.println("A");
			i++;
		}
		
		//if we want to print 11 table
		
		int j=11;
		while(j<=110)
		{
			System.out.println(j);
			j=j+11;
		}
		
		//if we want to print 11 table in reverse order
		
		int k=110;
		while(k>=11)
		{
			System.out.println(k);
			k=k-11;
		}
		
	}

}
