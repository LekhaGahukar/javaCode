package loops;

public class ForStudy {

	public static void main(String[] args) {
		
		//if we want to print A 10 times
		for(int i=1;i<=10;i++)
		{
			System.out.println("A");
		}
		//if we want to print table of 11
		
		for(int m=11;m<=110;m=m+11)
		{
			System.out.println(m);
		}
		
		//if we want to print 11 table in reverse order
		for(int j=110;j>=11;j=j-11)
		{
			System.out.println(j);
		}

	}

}
