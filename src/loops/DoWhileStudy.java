package loops;

public class DoWhileStudy {

	public static void main(String[] args) {
		int a=3;
		do
		{
			System.out.println(a);
			a=a+3;
		}while(a<=30);
		
		System.out.println("=================================================");
		
		int b=30;
		do
		{
			System.out.println(b);
			b=b-3;			
		}while(b>=3);

	}

}
