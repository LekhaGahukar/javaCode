package practice;

public class Swapping_3 {

	public static void main(String[] args) 
	{
		int a=10,b=20,temp;
		System.out.println("Value of A Before Swapping "+a);
		System.out.println("Value of B Before Swapping "+b);
	
	    temp=a;
	    a=b;
		b=temp;
		System.out.println("Value of A Before Swapping "+a);
		System.out.println("Value of B Before Swapping "+b);
		

	}

}
